IF EXISTS (SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_CATALOG = 'BusinessSafe' AND TABLE_NAME = 'Action' AND COLUMN_NAME = 'Status')
BEGIN
	ALTER TABLE Action
	DROP COLUMN Status
END
GO

